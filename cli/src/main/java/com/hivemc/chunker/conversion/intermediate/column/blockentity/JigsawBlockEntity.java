package com.hivemc.chunker.conversion.intermediate.column.blockentity;

/**
 * Represents a Jigsaw Block Entity.
 */
public class JigsawBlockEntity extends BlockEntity {
    /**
     * The joint type used when this jigsaw connects to another.
     */
    public enum Joint {
        ROLLABLE,
        ALIGNED
    }

    private String name = "minecraft:empty";
    private String target = "minecraft:empty";
    private String pool = "minecraft:empty";
    private String finalState = "minecraft:air";
    private Joint joint = Joint.ROLLABLE;
    private int selectionPriority;
    private int placementPriority;

    /**
     * Get the name of the jigsaw block.
     *
     * @return the name.
     */
    public String getName() {
        return name;
    }

    /**
     * Set the name of the jigsaw block.
     *
     * @param name the name.
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Get the target name this jigsaw connects to.
     *
     * @return the target name.
     */
    public String getTarget() {
        return target;
    }

    /**
     * Set the target name this jigsaw connects to.
     *
     * @param target the target name.
     */
    public void setTarget(String target) {
        this.target = target;
    }

    /**
     * Get the target pool to select a structure from.
     *
     * @return the target pool.
     */
    public String getPool() {
        return pool;
    }

    /**
     * Set the target pool to select a structure from.
     *
     * @param pool the target pool.
     */
    public void setPool(String pool) {
        this.pool = pool;
    }

    /**
     * Get the final block this jigsaw block becomes once generation completes.
     *
     * @return the final block state identifier.
     */
    public String getFinalState() {
        return finalState;
    }

    /**
     * Set the final block this jigsaw block becomes once generation completes.
     *
     * @param finalState the final block state identifier.
     */
    public void setFinalState(String finalState) {
        this.finalState = finalState;
    }

    /**
     * Get the joint type used when this jigsaw connects to another.
     *
     * @return the joint type.
     */
    public Joint getJoint() {
        return joint;
    }

    /**
     * Set the joint type used when this jigsaw connects to another.
     *
     * @param joint the joint type.
     */
    public void setJoint(Joint joint) {
        this.joint = joint;
    }

    /**
     * Get the priority of this jigsaw block being selected for generation.
     *
     * @return the selection priority.
     */
    public int getSelectionPriority() {
        return selectionPriority;
    }

    /**
     * Set the priority of this jigsaw block being selected for generation.
     *
     * @param selectionPriority the selection priority.
     */
    public void setSelectionPriority(int selectionPriority) {
        this.selectionPriority = selectionPriority;
    }

    /**
     * Get the priority of the piece generated by this jigsaw block to place its children.
     *
     * @return the placement priority.
     */
    public int getPlacementPriority() {
        return placementPriority;
    }

    /**
     * Set the priority of the piece generated by this jigsaw block to place its children.
     *
     * @param placementPriority the placement priority.
     */
    public void setPlacementPriority(int placementPriority) {
        this.placementPriority = placementPriority;
    }
}
